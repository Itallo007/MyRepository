    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        // Verifica se os campos estão OK
        if(this.verificar()){
            
            this.produto.setReferencia(this.txtReferencia.getText());
            this.produto.setNomeFornecedor(this.txtNomeForn.getText());
            this.produto.setQuantidade(Integer.parseInt(this.spnQuantidade.getValue().toString()));
            this.produto.setValorPago(Float.parseFloat(this.spnValorPago.getValue().toString()));
            this.produto.setValorVenda(Float.parseFloat(this.spnValorVenda.getValue().toString()));
            
            if(this.regra.jaExiste(produto)){
                this.regra.atualizar(produto);
            }else{
                this.regra.salvar(produto);
            }

            this.dispose();// Fecha a janela cadastro
            inicial.atualizar();// Atualiza a lista da tela inicial
        }
        
    }//GEN-LAST:event_btnSalvarActionPerformed

    

