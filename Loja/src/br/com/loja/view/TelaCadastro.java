    // Verifica se os campos Referência e Nome do fornecedor não estão vazios
    public boolean verificar(){
        if(this.txtReferencia.getText().equals("")){
            JOptionPane.showMessageDialog(null, "O campo Referência deve ser preenchido", "Aviso", JOptionPane.ERROR_MESSAGE);
            return false;
        }else if(this.txtNomeForn.getText().equals("")){
            JOptionPane.showMessageDialog(null, "O campo Nome do fornecedor deve ser preenchido", "Aviso", JOptionPane.ERROR_MESSAGE);
            return false;
        }else{
            return true;
        }
    }

